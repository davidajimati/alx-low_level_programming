#include "3-calc.h"
#include <stdlib.h>

/**
 * op_add - adds a, b
 *
 * @a: first integer
 * @b: second integer
 * Return: result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b);
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
